// Напишите программу, которая принимает на вход число (N) и выдаёт таблицу квадратов чисел от 1 до N.

// 5 -> 1, 4, 9, 16, 25.

// 2 -> 1, 4

Console.Clear();
Console.Write("Введите число: ");
int N = int.Parse(Console.ReadLine()!);
int i = 1;// 
int m = N; // например если  m = -10 то N = -1    эта строчка для второго решения 
if (N < 0) // это проверка на отрицательные числа
{
  i = N; // например i = -10
  m = -N; // то m = 10 чтобы создать массив и в циклах сделать нужное кол-во шагов эта строчка для второго решения
  N = -1; // тоже что и N = N * (-1) ВОЗВРАЩАЕТ отрицательное число
}

// первое решение 
