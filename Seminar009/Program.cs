// }

//решаем через массив
int[] result = new int[m];// создаём массив на m элементов
int j = 0;// создали переменную, она нужна для того что бы если например N отрицательная, то цикл будет например от -10 до -1 и по этим элементам мы не сможем обращатся к массиву

for (; i <= N; i++)
{
  result[j] = i * i;// через переменную j обращаемся к элементам массива и в result кладём i возведённая в квадрат
  j++;
}
for (j = 0; j < m; j++)// цикл перебирает все элементы массива 
{
  Console.Write($"{result[j]} ");// и выводит их на экран
}
